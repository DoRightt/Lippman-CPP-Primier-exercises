
int main() {
    'a';   // char
    L'a';  // wchar_t
    "a";   // string literal
    L"a";  // wide string literal

    10;    // int
    10u;   // unsigned int
    10L;   // long int
    10uL;  // unsigned long int
    012;   // int
    0xC;   // int

    3.14;   // double
    3.14f;  // float
    3.14L;  // long double

    10;     // int
    10u;    // unsigned int
    10.;    // double
    10e-2;  // double

    return 0;
}